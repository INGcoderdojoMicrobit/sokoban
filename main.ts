function doMove (mySprite: Sprite) {
    petla = 8
    dx = nast1.x - sokoban.tilemapLocation().x
    dy = nast1.y - sokoban.tilemapLocation().y
    for (let index = 0; index < petla; index++) {
        mySprite.x += dx / petla
        mySprite.y += dy / petla
        pause(400 / petla)
    }
}
